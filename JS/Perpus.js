        // Save user changes
        function saveUserChanges() {
            const newUsername = editUserUsernameInput.value.trim();
            const newPassword = editUserPasswordInput.value.trim();
            const users = JSON.parse(localStorage.getItem('users')) || [];
            const userIndex = users.findIndex(u => u.username === currentEditUsername);

            if (userIndex === -1) {
                Swal.fire({
                    title: 'Error',
                    text: 'Pengguna tidak ditemukan!',
                    icon: 'error',
                    confirmButtonColor: '#3498db'
                });
                return;
            }

            if (!newUsername) {
                Swal.fire({
                    title: 'Error',
                    text: 'Username tidak boleh kosong!',
                    icon: 'error',
                    confirmButtonColor: '#3498db'
                });
                return;
            }

            // Check if new username is already taken
            if (newUsername !== currentEditUsername && users.some(u => u.username === newUsername)) {
                Swal.fire({
                    title: 'Error',
                    text: 'Username sudah digunakan!',
                    icon: 'error',
                    confirmButtonColor: '#3498db'
                });
                return;
            }

            // Update user data
            users[userIndex].username = newUsername;
            if (newPassword) {
                users[userIndex].password = newPassword;
            }

            // Update borrowedBooks to reflect new username
            borrowedBooks = borrowedBooks.map(book => {
                if (book.borrower === currentEditUsername) {
                    return { ...book, borrower: newUsername };
                }
                return book;
            });

            // Update current user session if applicable
            const currentUser = JSON.parse(localStorage.getItem('user'));
            if (currentUser && currentUser.username === currentEditUsername) {
                localStorage.setItem('user', JSON.stringify({ username: newUsername, isAdmin: currentUser.isAdmin }));
            }

            // Save changes to localStorage
            localStorage.setItem('users', JSON.stringify(users));
            localStorage.setItem('borrowedBooks', JSON.stringify(borrowedBooks));

            // Update UI
            updateUserManagementTable();
            updateBorrowedTable();
            updateAdminBorrowedTable();
            userEditModal.style.display = 'none';

            Swal.fire({
                title: 'Pengguna Diperbarui',
                text: `Data pengguna "${newUsername}" telah diperbarui.`,
                icon: 'success',
                confirmButtonColor: '#3498db'
            });
        }

