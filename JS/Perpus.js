        // Update borrowed books table for regular users
        function updateBorrowedTable() {
            borrowedTable.innerHTML = '';
            const user = JSON.parse(localStorage.getItem('user'));
            const userBorrowedBooks = user && !user.isAdmin ? borrowedBooks.filter(b => b.borrower === user.username) : [];

            if (userBorrowedBooks.length === 0) {
                borrowedTable.innerHTML = `
                    <tr>
                        <td colspan="5" style="text-align: center;">Tidak ada buku yang dipinjam.</td>
                    </tr>
                `;
                return;
            }

            userBorrowedBooks.forEach(book => {
                const today = new Date();
                const returnDate = new Date(book.returnDate);
                const daysLeft = Math.ceil((returnDate - today) / (1000 * 60 * 60 * 24));
                
                let status, statusClass;
                if (daysLeft < 0) {
                    status = `Terlambat ${Math.abs(daysLeft)} hari`;
                    statusClass = 'overdue';
                } else if (daysLeft <= 3) {
                    status = `${daysLeft} hari lagi`;
                    statusClass = 'warning';
                } else {
                    status = `${daysLeft} hari lagi`;
                    statusClass = 'normal';
                }

                const row = document.createElement('tr');
                row.innerHTML = `
                    <td>${book.title}</td>
                    <td>${formatDate(book.borrowDate)}</td>
                    <td class="return-date ${statusClass}">${formatDate(book.returnDate)}</td>
                    <td>${status}</td>
                    <td>
                        <button class="btn btn-return" data-id="${book.id}">Kembalikan</button>
                        <button class="btn btn-details" data-id="${book.id}">Detail</button>
                    </td>
                `;

                borrowedTable.appendChild(row);
            });

            // Add event listeners to buttons
            document.querySelectorAll('.borrowed-table .btn-return').forEach(btn => {
                btn.addEventListener('click', (e) => {
                    const bookId = parseInt(e.target.getAttribute('data-id'));
                    
                    Swal.fire({
                        title: 'Kembalikan Buku?',
                        text: "Apakah Anda yakin ingin mengembalikan buku ini?",
                        icon: 'question',
                        showCancelButton: true,
                        confirmButtonColor: '#3498db',
                        cancelButtonColor: '#e74c3c',
                        confirmButtonText: 'Ya, Kembalikan',
                        cancelButtonText: 'Batal'
                    }).then((result) => {
                        if (result.isConfirmed) {
                            returnBook(bookId);
                        }
                    });
                });
            });

            document.querySelectorAll('.borrowed-table .btn-details').forEach(btn => {
                btn.addEventListener('click', (e) => {
                    const bookId = parseInt(e.target.getAttribute('data-id'));
                    showBookDetails(bookId);
                });
            });
        }

