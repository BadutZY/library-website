        // Update authentication button state
        function updateAuthButton() {
            const user = JSON.parse(localStorage.getItem('user'));
            if (user) {
                authButton.textContent = 'Logout';
                authButton.href = '#';
                authButton.onclick = handleLogout;
            } else {
                authButton.textContent = 'Login';
                authButton.href = 'login.html';
                authButton.onclick = null;
            }
        }

        // Handle logout
        function handleLogout() {
            Swal.fire({
                title: 'Konfirmasi Logout',
                text: 'Apakah Anda yakin ingin logout?',
                icon: 'question',
                showCancelButton: true,
                confirmButtonColor: '#3498db',
                cancelButtonColor: '#e74c3c',
                confirmButtonText: 'Ya, Logout',
                cancelButtonText: 'Batal'
            }).then((result) => {
                if (result.isConfirmed) {
                    localStorage.removeItem('user');
                    updateAuthButton();
                    toggleTabsVisibility();
                    updateBorrowedTable();
                    renderBooks(books);
                    Swal.fire({
                        title: 'Logout Berhasil',
                        text: 'Anda telah logout dari sistem.',
                        icon: 'success',
                        confirmButtonColor: '#3498db'
                    });
                }
            });
        }

        // Toggle tabs visibility based on user role
        function toggleTabsVisibility() {
            const user = JSON.parse(localStorage.getItem('user'));
            const adminTabs = document.querySelectorAll('.tab-btn.admin-only');
            const userTabs = document.querySelectorAll('.tab-btn.user-only');

            if (user && user.isAdmin) {
                // Show admin tabs, hide user tabs
                adminTabs.forEach(tab => {
                    tab.style.display = 'block';
                });
                userTabs.forEach(tab => {
                    tab.style.display = 'none';
                    if (tab.classList.contains('active')) {
                        switchTab('catalog');
                    }
                });
            } else {
                // Show user tabs, hide admin tabs
                adminTabs.forEach(tab => {
                    tab.style.display = 'none';
                    if (tab.classList.contains('active')) {
                        switchTab('catalog');
                    }
                });
                userTabs.forEach(tab => {
                    tab.style.display = 'block';
                });
            }
        }

        // Setup all event listeners
        function setupEventListeners() {
            // Tab switching
            tabButtons.forEach(button => {
                button.addEventListener('click', () => {
                    const tabId = button.getAttribute('data-tab');
                    switchTab(tabId);
                });
            });

            // Close modals
            closeModal.forEach(btn => {
                btn.addEventListener('click', () => {
                    modal.style.display = 'none';
                    userEditModal.style.display = 'none';
                });
            });

            // Close modals when clicking outside
            window.addEventListener('click', (e) => {
                if (e.target === modal) {
                    modal.style.display = 'none';
                }
                if (e.target === userEditModal) {
                    userEditModal.style.display = 'none';
                }
            });

            // Genre filter
            genreFilter.addEventListener('change', filterBooks);

            // Search function
            searchButton.addEventListener('click', filterBooks);
            searchInput.addEventListener('keyup', (e) => {
                if (e.key === 'Enter') {
                    filterBooks();
                }
            });

            // Mobile menu toggle
            menuToggle.addEventListener('click', () => {
                nav.classList.toggle('active');
                overlay.classList.toggle('active');
            });

            // Close mobile menu when clicking overlay
            overlay.addEventListener('click', () => {
                nav.classList.remove('active');
                overlay.classList.remove('active');
            });
        }

        // Switch between tabs
        function switchTab(tabId) {
            tabButtons.forEach(btn => {
                btn.classList.remove('active');
                if (btn.getAttribute('data-tab') === tabId) {
                    btn.classList.add('active');
                }
            });

            tabContents.forEach(content => {
                content.classList.remove('active');
                if (content.id === tabId) {
                    content.classList.add('active');
                }
            });

            // Update tables when switching tabs
            if (tabId === 'borrowed') {
                updateBorrowedTable();
            } else if (tabId === 'admin-borrowed') {
                updateAdminBorrowedTable();
            } else if (tabId === 'user-management') {
                updateUserManagementTable();
            }
        }

        // Render books to the grid
        function renderBooks(booksToRender) {
            bookGrid.innerHTML = '';
            
            if (booksToRender.length === 0) {
                bookGrid.innerHTML = '<p>Tidak ada buku yang ditemukan.</p>';
                return;
            }

            const user = JSON.parse(localStorage.getItem('user'));
            const isAdmin = user && user.isAdmin;

            booksToRender.forEach(book => {
                const bookCard = document.createElement('div');
                bookCard.className = 'book-card';
                bookCard.setAttribute('data-id', book.id);

                const isBorrowed = borrowedBooks.some(b => b.id === book.id);
                let buttonHtml = '';

                if (!isAdmin) {
                    // Show borrow button for non-admin users
                    const buttonClass = isBorrowed ? 'btn-disabled' : 'btn-borrow';
                    const buttonText = isBorrowed ? 'Dipinjam' : 'Pinjam';
                    const buttonDisabled = isBorrowed ? 'disabled' : '';
                    buttonHtml = `<button class="btn ${buttonClass}" ${buttonDisabled} data-id="${book.id}">${buttonText}</button>`;
                }

                bookCard.innerHTML = `
                    <img src="${book.cover}" alt="${book.title}" class="book-cover">
                    <div class="book-info">
                        <h3 class="book-title">${book.title}</h3>
                        <p class="book-publisher">${book.publisher}</p>
                        <div class="book-actions">
                            <button class="btn btn-details" data-id="${book.id}">Detail</button>
                            ${buttonHtml}
                        </div>
                    </div>
                `;

                bookGrid.appendChild(bookCard);
            });

            // Add event listeners to buttons
            document.querySelectorAll('.btn-details').forEach(btn => {
                btn.addEventListener('click', (e) => {
                    const bookId = parseInt(e.target.getAttribute('data-id'));
                    showBookDetails(bookId);
                });
            });

            document.querySelectorAll('.btn-borrow').forEach(btn => {
                btn.addEventListener('click', (e) => {
                    const bookId = parseInt(e.target.getAttribute('data-id'));
                    borrowBook(bookId);
                });
            });
        }

        // Filter books by genre and search term
        function filterBooks() {
            const selectedGenre = genreFilter.value;
            const searchTerm = searchInput.value.toLowerCase();

            let filteredBooks = books;

            if (selectedGenre !== 'all') {
                filteredBooks = filteredBooks.filter(book => book.genre === selectedGenre);
            }

            if (searchTerm) {
                filteredBooks = filteredBooks.filter(book => 
                    book.title.toLowerCase().includes(searchTerm) || 
                    book.publisher.toLowerCase().includes(searchTerm)
                );
            }

            renderBooks(filteredBooks);
        }

